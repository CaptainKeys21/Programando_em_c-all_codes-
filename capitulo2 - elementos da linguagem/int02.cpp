#include <iostream>
using namespace std;
int main()
{
    cout << "short int = " << sizeof(short) << " bytes" << endl;
    cout << "int = " << sizeof(int) << " bytes" << endl;
    cout << "long int = " << sizeof(long) << " bytes" << endl;
    cout << "long long int = " << sizeof(long long) << " bytes" << endl;
    cout << "char = " << sizeof(char) << " byte" << endl;
    return 0;
}